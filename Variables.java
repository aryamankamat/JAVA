
public class Variables {
    public static void main(String[] args) {
        byte var1 = 10; // byte data type.
        System.out.println(var1);
        short var2 = 20; // short data type.
        System.out.println(var2);
        long var6 = 5666666666L; // long data type.
        System.out.println(var6);
        int var3 = 30; // interger data type.
        System.out.println(var3);
        float var4 = 99.9f; // floating point data type.
        System.out.println(var4);
        double var5 = 1001.10d; // double data type.
        System.out.println(var5);
        char a = 'A'; // character data type.
        System.out.println(a);
        boolean var7 = true; // Boolean datatype.
        System.out.println(var7);
        String var8 = "Aryaman";
        System.out.println(var8);
    }
}
